type NicheStockInput = Omit<
  Stock,
  "logo" | "day" | "week" | "month" | "year" | "pe" | "eps" | "dividend" | "high52" | "low52" | "recommendation" | "insider" | "chart"
> &
  Partial<Pick<Stock, "logo" | "week" | "month" | "year" | "pe" | "eps" | "dividend" | "high52" | "low52" | "recommendation" | "insider" | "chart">>;

const createNicheStock = (stock: NicheStockInput): Stock => {
  const eps = stock.eps ?? 0;
  const recommendation =
    stock.recommendation ?? (stock.aiScore >= 8 ? "Buy" : stock.aiScore >= 5 ? "Hold" : "Avoid");

  return {
    ...stock,
    logo: stock.logo ?? stock.ticker.replace(".", "").slice(0, 2).toUpperCase(),
    day: stock.change,
    week: stock.week ?? Number((stock.change * 1.7).toFixed(1)),
    month: stock.month ?? Number((stock.change * 3.1).toFixed(1)),
    year: stock.year ?? Number((stock.revenueGrowth + stock.earningsGrowth - 12).toFixed(1)),
    pe: stock.pe ?? (eps > 0 ? Number((stock.price / eps).toFixed(1)) : 0),
    eps,
    dividend: stock.dividend ?? "0.00%",
    high52: stock.high52 ?? Number((stock.price * 1.42).toFixed(2)),
    low52: stock.low52 ?? Number((stock.price * 0.62).toFixed(2)),
    recommendation,
    insider: stock.insider ?? "Neutral",
    chart:
      stock.chart ??
      [0.74, 0.79, 0.84, 0.9, 0.96, 1.03, 1.08, 1.13, 1.05, 1].map((multiplier) =>
        Number((stock.price * multiplier).toFixed(2))
      )
  };
};

